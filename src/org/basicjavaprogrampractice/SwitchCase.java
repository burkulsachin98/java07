package org.basicjavaprogrampractice;

public class SwitchCase {

	public static void main(String[] args) {

		int day = 35;
		switch (day) {

		case 1:
			System.out.println("Mon");
			break;
		case 2:
			System.out.println("Tue");	
			break;
		case 3:
			System.out.println("Wed");
			break;
		default:
			System.out.println("Invalid Day");
		}

	}

}
